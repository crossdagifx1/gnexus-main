/**
 * Analyzes the user prompt to determine the best layout intent
 */
export function analyzeIntent(prompt: string): NeuralIntent {
    const p = prompt.toLowerCase();

    if (p.includes('vs') || p.includes('compare') || p.includes('difference') || p.includes('alternatives')) {
        return 'comparison';
    }
    if (p.includes('history') || p.includes('roadmap') || p.includes('evolution') || p.includes('timeline')) {
        return 'timeline';
    }
    if (p.includes('growth') || p.includes('scale') || p.includes('market size') || p.includes('revenue')) {
        return 'growth';
    }
    if (p.includes('ecosystem') || p.includes('stack') || p.includes('architecture')) {
        return 'ecosystem';
    }

    return 'analysis';
}

/**
 * Validates if the content matches the NeuralContent schema
 */
function validateNeuralContent(content: any): content is NeuralContent {
    return (
        typeof content === 'object' &&
        typeof content.title === 'string' &&
        typeof content.subtitle === 'string' &&
        typeof content.intent === 'string' &&
        typeof content.brief === 'string' &&
        Array.isArray(content.sections) &&
        Array.isArray(content.metrics)
    );
}

