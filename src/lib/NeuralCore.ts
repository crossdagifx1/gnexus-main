/**
 * Generates high-fidelity structured data using AI with self-correction
 */
export async function generateNeuralContent(prompt: string): Promise<NeuralContent> {
    const p = prompt.toLowerCase();
    const intent = analyzeIntent(prompt);

    // Construct a strict system prompt for JSON generation
    const systemPrompt = `You are a Neural Content Generator. Generate a valid JSON object matching this TypeScript interface:

interface NeuralContent {
    title: string;
    subtitle: string;
    intent: '${intent}';
    brief: string;
    sections: { title: string; content: string[]; type: 'text' | 'list' | 'cards' | 'steps' }[];
    metrics: { label: string; value: string; trend: 'up' | 'down' | 'neutral'; delta: string }[];
    chart?: { title: string; type: 'bar' | 'line' | 'radar'; data: { labels: string[]; datasets: { label: string; data: number[]; color?: string }[] } };
    comparison?: { items: string[]; features: { name: string; values: (boolean | string)[] }[] };
}

CRITICAL: Return ONLY valid JSON. No markdown, no explanation, just the JSON object.`;

    const userPrompt = `Generate comprehensive neural content for: "${prompt}"

Guidelines:
- Make the title compelling and specific to the topic
- Include 2-4 detailed sections with rich content
- Provide 4 realistic metrics with trends
- If the topic involves comparison, add the comparison object
- If data visualization makes sense, include a chart

Topic: ${prompt}`;

    // Attempt AI generation with retry logic
    const maxRetries = 3;
    let lastError = '';

    for (let attempt = 0; attempt < maxRetries; attempt++) {
        try {
            console.log(`[NeuralCore] AI Generation attempt ${attempt + 1}/${maxRetries}`);

            const response = await generateText('analyst', userPrompt, {
                temperature: 0.7,
                max_new_tokens: 3000
            });

            if (!response.success || !response.data) {
                lastError = response.error || 'Empty response';
                console.warn(`[NeuralCore] Attempt ${attempt + 1} failed:`, lastError);
                continue;
            }

            // Clean up response (remove markdown code blocks if present)
            let jsonText = response.data.trim();
            if (jsonText.startsWith('```')) {
                jsonText = jsonText.replace(/^```(?:json)?\n?/i, '').replace(/\n?```$/, '');
            }

            // Parse JSON
            const parsed = JSON.parse(jsonText);

            // Validate schema
            if (validateNeuralContent(parsed)) {
                console.log('[NeuralCore] ✓ AI generation successful');
                return parsed;
            } else {
                lastError = 'Schema validation failed';
                console.warn('[NeuralCore] Invalid schema, attempting self-correction...');

                // Self-correction attempt
                if (attempt < maxRetries - 1) {
                    const correctionPrompt = `The following JSON is invalid according to the NeuralContent schema. Fix it and return ONLY the corrected JSON:

${jsonText}

Required schema: ${systemPrompt}`;

                    const corrected = await generateText('analyst', correctionPrompt);
                    if (corrected.success && corrected.data) {
                        const fixedJson = corrected.data.trim().replace(/^```(?:json)?\n?/i, '').replace(/\n?```$/, '');
                        const fixedParsed = JSON.parse(fixedJson);
                        if (validateNeuralContent(fixedParsed)) {
                            console.log('[NeuralCore] ✓ Self-correction successful');
                            return fixedParsed;
                        }
                    }
                }
            }
        } catch (error: any) {
            lastError = error.message;
            console.warn(`[NeuralCore] Attempt ${attempt + 1} error:`, error.message);
        }
    }

    // All attempts failed, use intelligent fallback
    console.warn('[NeuralCore] All AI attempts failed, using fallback generator');
    console.warn('[NeuralCore] Last error:', lastError);

    return generateFallbackContent(prompt);
}
