export type NeuralIntent = 'comparison' | 'timeline' | 'analysis' | 'growth' | 'ecosystem';

export interface NeuralMetric {
    label: string;
    value: string;
    trend: 'up' | 'down' | 'neutral';
    delta: string;
}

export interface NeuralChartData {
    labels: string[];
    datasets: {
        label: string;
        data: number[];
        color?: string;
    }[];
}

export interface NeuralContent {
    title: string;
    subtitle: string;
    intent: NeuralIntent;
    brief: string;
    sections: {
        title: string;
        content: string[];
        type: 'text' | 'list' | 'cards' | 'steps';
    }[];
    metrics: NeuralMetric[];
    chart?: {
        title: string;
        type: 'bar' | 'line' | 'radar';
        data: NeuralChartData;
    };
    comparison?: {
        items: string[];
        features: { name: string; values: boolean[] | string[] }[];
    };
}

