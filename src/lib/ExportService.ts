/**
 * G-NEXUS Export Service
 * 
 * Provides export functionality for workflows in multiple formats:
 * - PDF (styled document)
 * - Markdown (developer-friendly)
 * - JSON (machine-readable)
 */

import { toast } from 'sonner';

// ============================================================================
// TYPES
// ============================================================================

export interface WorkflowExportData {
    goal: string;
    blueprint?: string;
    research: Array<{
        label: string;
        content: string;
        status: string;
    }>;
    generatedCode?: Array<{
        name: string;
        path: string;
        content: string;
        language: string;
    }>;
    htmlPreview?: string;
    timestamp: Date;
    nodeCount: number;
}

export type ExportFormat = 'pdf' | 'markdown' | 'json' | 'html';

export interface ExportOptions {
    format: ExportFormat;
    includeCode?: boolean;
    includePreview?: boolean;
    theme?: 'light' | 'dark';
}

// ============================================================================
// MARKDOWN EXPORT
// ============================================================================

export function exportToMarkdown(data: WorkflowExportData): string {
    const sections: string[] = [];

    // Header
    sections.push(`# ${data.goal}\n`);
    sections.push(`**Generated**: ${data.timestamp.toLocaleString()}\n`);
    sections.push(`**Nodes**: ${data.nodeCount}\n`);
    sections.push('---\n');

    // Blueprint
    if (data.blueprint) {
        sections.push('## 🎯 Blueprint\n');
        sections.push(data.blueprint);
        sections.push('\n---\n');
    }

    // Research Results
    if (data.research.length > 0) {
        sections.push('## 🔍 Research Results\n');
        data.research.forEach((r, idx) => {
            sections.push(`### ${idx + 1}. ${r.label}\n`);
            sections.push(`**Status**: ${r.status}\n`);
            sections.push(r.content);
            sections.push('\n');
        });
        sections.push('---\n');
    }

    // Generated Code
    if (data.generatedCode && data.generatedCode.length > 0) {
        sections.push('## 💻 Generated Code\n');
        data.generatedCode.forEach(file => {
            sections.push(`### \`${file.name}\`\n`);
            sections.push(`**Path**: \`${file.path}\`\n\n`);
            sections.push(`\`\`\`${file.language}\n`);
            sections.push(file.content);
            sections.push('\n```\n\n');
        });
        sections.push('---\n');
    }

    // Footer
    sections.push('\n*Generated by G-NEXUS AI Workflow System*\n');

    return sections.join('\n');
}

// ============================================================================
// JSON EXPORT
// ============================================================================

export function exportToJSON(data: WorkflowExportData): string {
    const exportObj = {
        version: '1.0',
        generator: 'G-NEXUS',
        timestamp: data.timestamp.toISOString(),
        workflow: {
            goal: data.goal,
            blueprint: data.blueprint,
            nodeCount: data.nodeCount
        },
        research: data.research,
        generatedCode: data.generatedCode,
        htmlPreview: data.htmlPreview
    };

    return JSON.stringify(exportObj, null, 2);
}

