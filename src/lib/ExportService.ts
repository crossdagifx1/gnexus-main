// ============================================================================
// PDF EXPORT (using jsPDF)
// ============================================================================

export async function exportToPDF(data: WorkflowExportData): Promise<Blob> {
    try {
        // Dynamic import to reduce bundle size
        const { default: jsPDF } = await import('jspdf');

        const doc = new jsPDF({
            orientation: 'portrait',
            unit: 'mm',
            format: 'a4'
        });

        let y = 20;
        const pageWidth = doc.internal.pageSize.getWidth();
        const margin = 20;
        const contentWidth = pageWidth - (margin * 2);

        // Title
        doc.setFontSize(24);
        doc.setTextColor(102, 126, 234); // Primary color
        doc.text(data.goal, margin, y);
        y += 10;

        // Metadata
        doc.setFontSize(10);
        doc.setTextColor(100, 100, 100);
        doc.text(`Generated: ${data.timestamp.toLocaleString()}`, margin, y);
        y += 5;
        doc.text(`Nodes: ${data.nodeCount}`, margin, y);
        y += 15;

        // Separator line
        doc.setDrawColor(102, 126, 234);
        doc.line(margin, y, pageWidth - margin, y);
        y += 10;

        // Blueprint
        if (data.blueprint) {
            doc.setFontSize(16);
            doc.setTextColor(0, 0, 0);
            doc.text('🎯 Blueprint', margin, y);
            y += 8;

            doc.setFontSize(10);
            doc.setTextColor(60, 60, 60);
            const blueprintLines = doc.splitTextToSize(data.blueprint, contentWidth);
            doc.text(blueprintLines, margin, y);
            y += blueprintLines.length * 5 + 10;

            // Add page if needed
            if (y > 270) {
                doc.addPage();
                y = 20;
            }
        }

        // Research Results
        if (data.research.length > 0) {
            doc.setFontSize(16);
            doc.setTextColor(0, 0, 0);
            doc.text('🔍 Research Results', margin, y);
            y += 8;

            data.research.forEach((r, idx) => {
                if (y > 250) {
                    doc.addPage();
                    y = 20;
                }

                doc.setFontSize(12);
                doc.setTextColor(118, 75, 162); // Secondary color
                doc.text(`${idx + 1}. ${r.label}`, margin, y);
                y += 6;

                doc.setFontSize(9);
                doc.setTextColor(60, 60, 60);
                const contentLines = doc.splitTextToSize(r.content.substring(0, 500) + '...', contentWidth);
                doc.text(contentLines, margin, y);
                y += contentLines.length * 4 + 8;
            });
        }

        // Generated Code
        if (data.generatedCode && data.generatedCode.length > 0) {
            if (y > 250) {
                doc.addPage();
                y = 20;
            }

            doc.setFontSize(16);
            doc.setTextColor(0, 0, 0);
            doc.text('💻 Generated Code', margin, y);
            y += 8;

            data.generatedCode.forEach(file => {
                if (y > 240) {
                    doc.addPage();
                    y = 20;
                }

                doc.setFontSize(11);
                doc.setTextColor(78, 201, 176); // Code color
                doc.text(file.name, margin, y);
                y += 6;

                doc.setFontSize(8);
                doc.setFont('courier');
                doc.setTextColor(40, 40, 40);
                const codeLines = doc.splitTextToSize(file.content.substring(0, 1000) + '...', contentWidth);
                doc.text(codeLines, margin, y);
                y += codeLines.length * 3.5 + 10;
                doc.setFont('helvetica');
            });
        }

        // Footer on last page
        const pageCount = doc.getNumberOfPages();
        for (let i = 1; i <= pageCount; i++) {
            doc.setPage(i);
            doc.setFontSize(8);
            doc.setTextColor(150, 150, 150);
            doc.text(
                `Generated by G-NEXUS | Page ${i} of ${pageCount}`,
                pageWidth / 2,
                doc.internal.pageSize.getHeight() - 10,
                { align: 'center' }
            );
        }

        return doc.output('blob');
    } catch (error) {
        console.error('[ExportService] PDF generation failed:', error);
        throw new Error('Failed to generate PDF. Make sure jsPDF is installed.');
    }
}

// ============================================================================
// DOWNLOAD HELPERS
// ============================================================================

export function downloadFile(content: string | Blob, filename: string, mimeType: string) {
    const blob = typeof content === 'string'
        ? new Blob([content], { type: mimeType })
        : content;

    const url = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = url;
    link.download = filename;
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
    URL.revokeObjectURL(url);
}

export async function exportWorkflow(data: WorkflowExportData, options: ExportOptions): Promise<void> {
    try {
        const timestamp = data.timestamp.toISOString().split('T')[0];
        const baseName = data.goal.replace(/[^a-z0-9]/gi, '_').toLowerCase().substring(0, 30);

        switch (options.format) {
            case 'markdown': {
                const md = exportToMarkdown(data);
                downloadFile(md, `${baseName}_${timestamp}.md`, 'text/markdown');
                toast.success('📄 Exported to Markdown!');
                break;
            }

            case 'json': {
                const json = exportToJSON(data);
                downloadFile(json, `${baseName}_${timestamp}.json`, 'application/json');
                toast.success('📊 Exported to JSON!');
                break;
            }

            case 'html': {
                const html = exportToHTML(data);
                downloadFile(html, `${baseName}_${timestamp}.html`, 'text/html');
                toast.success('🌐 Exported to HTML!');
                break;
            }

            case 'pdf': {
                toast.info('📄 Generating PDF...');
                const pdfBlob = await exportToPDF(data);
                downloadFile(pdfBlob, `${baseName}_${timestamp}.pdf`, 'application/pdf');
                toast.success('📄 Exported to PDF!');
                break;
            }

            default:
                throw new Error(`Unknown export format: ${options.format}`);
        }
    } catch (error: any) {
        console.error('[ExportService] Export failed:', error);
        toast.error(`Export failed: ${error.message}`);
        throw error;
    }
}

// ============================================================================
// UTILITIES
// ============================================================================

function escapeHtml(text: string): string {
    const map: Record<string, string> = {
        '&': '&amp;',
        '<': '&lt;',
        '>': '&gt;',
        '"': '&quot;',
        "'": '&#039;'
    };
    return text.replace(/[&<>"']/g, m => map[m]);
}
